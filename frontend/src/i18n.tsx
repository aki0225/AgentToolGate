export type TranslationKey = keyof (typeof messages)["en-US"];

type TranslationValues = Record<string, string | number>;

type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, values?: TranslationValues) => string;
};

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const initialLocale = resolveInitialLocale();
    syncDocumentLocale(initialLocale);
    return initialLocale;
  });

  function setLocale(nextLocale: Locale) {
    setLocaleState(nextLocale);
    syncDocumentLocale(nextLocale);
    try {
      window.localStorage.setItem(localeStorageKey, nextLocale);
    } catch {
      // localStorage can be unavailable in privacy-restricted contexts.
    }
  }

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key, values) => formatMessage(messages[locale][key] ?? messages["en-US"][key], values),
    }),
    [locale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return value;
}

function resolveInitialLocale(): Locale {
  const stored = safeStoredLocale();
  if (stored) {
    return stored;
  }
  const browserLanguage = window.navigator.language.toLowerCase();
  return browserLanguage.startsWith("zh") ? "zh-CN" : "en-US";
}

function safeStoredLocale(): Locale | null {
  try {
    const stored = window.localStorage.getItem(localeStorageKey);
    return isSupportedLocale(stored) ? stored : null;
  } catch {
    return null;
  }
}

function isSupportedLocale(value: string | null): value is Locale {
  return supportedLocales.some((locale) => locale === value);
}

function syncDocumentLocale(locale: Locale) {
  document.documentElement.lang = locale;
}

function formatMessage(message: string, values?: TranslationValues): string {
  if (!values) {
    return message;
  }
  return Object.entries(values).reduce(
    (current, [key, value]) => current.replaceAll(`{${key}}`, String(value)),
    message,
  );
}
